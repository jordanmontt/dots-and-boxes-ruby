class Line
    def initialize(x1,y1,x2,y2,id)
        @x1 = x1
        @y1 = y1
        @x2 = x2
        @y2 = y2
        @id = id
    end

    def x1
        @x1
    end

    def y1
        @y1
    end

    def x2
        @x2
    end

    def y2
        @y2
    end

    def id
        @id
    end
end